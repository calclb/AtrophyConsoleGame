package me.ubuntoof;

public class StatContainer
{
    public final double stamina;
    public final double strength;
    public final double intelligence;
    public final double dexterity;
    public final double perception;
    public final double stealth;
    public final double agility;

    public StatContainer(double stamina, double strength, double intelligence, double dexterity, double perception, double stealth, double agility)
    {
        this.stamina = stamina;
        this.strength = strength;
        this.intelligence = intelligence;
        this.dexterity = dexterity;
        this.perception = perception;
        this.stealth = stealth;
        this.agility = agility;
    }
}
